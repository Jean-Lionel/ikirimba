						<div class="main-content" role="main">
							<p>
								
								L’association des enfants et jeunes travailleurs du Burundi (AEJT-Burundi) est une structure des enfants et jeunes qui ont pris l’engagement d’être acteurs de leur  développement. Pour y arriver, ils se sont organisés dans les groupements de Bases depuis les sous-collines, les collines en passant par les coordinations communales et provinciales jusqu’au niveau national.

							</p>

							<p>
								L’AEJT-Burundi dans ses activités cherche à prioriser le renforcement des capacités de ses acteurs à la base. Ses acteurs sont composés de la plupart des enfants et jeunes qui exercent des petits métiers dans les marchés, les enfants et jeunes apprentis,  les enfants et jeunes victimes de la traite, les déplacés, les mendiants etc.…
							</p>

							<h2>1. Sensibilisation</h2>
							<p>
								Pour mieux connaitre l’association, nous devrions faire une sensibilisation auprès des Enfants et Jeunes sur la mission et les objectifs de notre association. La sensibilisation s’est donc faite de deux façons : D’abord au niveau de l’Administration Locale et celle-ci nous ont permis d’affronter la population cible. Ladite sensibilisation a été faite dans neuf provinces  dont Bujumbura Mairie, Bujumbura Rural, Ruyigi, Kirundo, Mwaro, Makamba, Cankuzo, Rumonge et Gitega.
							</p>

							<p>
								Après la sensibilisation nous procédons à la constitution des groupes de base sur chaque sous colline de ces communes. Soulignons que cette activité est toujours en cours ; elle sera terminée après avoir contourné tout le territoire Burundais. Nous avons pu ouvrir des bureaux dans la province Ruyigi en commune Ruyigi au quartier Sanzu et en commune Gisuru ainsi qu’au quartier Magarama de la commune Gitega dans la province de Gitega dont vous êtes Responsable comme vous le voyez sur les photos.
							</p>

							<h1>2. Formations sur la création des A.G.R</h1>

							<p>

								Après  la constitution de chaque groupe de base, Il nous  fallait une organisation des  formations dans tous les domaines et à nos jeunes du groupe et surtout une formation sur la lutte contre la pauvreté et le chômage : 
								- Nous avons donné des micro-crédits aux 400 membres pour la réalisation des A.G.R dans le projet Fraternité, Contribution et Crédit (F.C.C) en sigle 
								- la distribution de 30 porcs dans le projet d’élevage. 
							</p>

